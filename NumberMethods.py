from math import *
print("Absolute Value Method:")
num = -6
print(abs(num)) #abs -> absolute method

print("Power Method:")
print(pow(4,8)) #pow -> power method

print("Maximum Value:")
print(max(10, 25)) #max -> maximum value

print("Minimum Value:")
print(min(20,50)) #min -> minimum value

print("Round Value:")
print(round(3.8)) #round -> round value
print(round(3.2))

print("Floor Method:")
print(floor(3.8)) #floor -> floor method
print(floor(3.4))

print("Ceil Mehthod:")
print(ceil(4.8)) #ceil -> ceil method
print(ceil(4.3))

print("Square Root Method:")
print(sqrt(49)) #sqrt -> square root method
print(sqrt(68))
